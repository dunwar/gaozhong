// ========== 批改标记识别规则（v2 新增） ==========

const GRADING_MARK_GUIDE = `═══════════════════════════════════════
【批改标记识别规则】⚠️ 关键章节 — 请严格按照以下规则判断每道题的批改状态
═══════════════════════════════════════

在中国高中试卷批改中，教师使用的标记有多种约定。OCR 文本中的【教师红笔批改：xxx】和【批改标记：xxx】段落专门标注了这些信息。

■ 一、批改标记含义速查表

| 标记 | 含义 | 判断结论 |
|------|------|---------|
| ✗ 或 ×（红色或黑色） | 该题/该步骤做错 | → 错题 |
| ✓ 或 √（红色或黑色） | 该题全部正确 | → 非错题，跳过 |
| 红色笔迹书写的答案/数字/公式 | 教师标注的**正确答案** | → 说明学生原答案有误 | 错题 |
| 红色圆圈 ○（圈出某个答案或步骤） | 该处有错误 | → 错题 |
| 红色下划线 ＿ 或波浪线 ～ | 标出错误所在 | → 错题，该处有误 |
| 扣分数字（如 -2、-3、-5、扣3分） | 该题被扣 N 分 | → 有错误（即使没画✗） |
| 得分标注（如 8/10、得8分） | 得分 < 满分 | → 有错误（满分-得分=扣分） |
| 红色"?" 或 "？" | 教师存疑/答案不完备 | → 可能扣分 → 归为错题 |
| 红色批注文字（如"不对""思路错误""注意审题""答非所问"） | 教师直接指出错误 | → 错题 |
| 半勾（√缺一笔）或✓带点 | 部分正确，部分错误 | → 归为错题（取未得分部分） |
| 大段红色划线（整段或整行） | 整段都有问题 | → 错题，该题有严重问题 |
| 数字被圈出（如 ④） | 教师标注该处数值或步骤有误 | → 错题 |

■ 二、判断优先级（从高到低）

1. **教师红笔标注 > 学生黑色笔迹** — 红笔批改是最终裁决
2. **明确错误标记（✗、×、扣分） > 暗示标记（?、批注文字）** — 先看有没有明确的错
3. **扣分数字 > 标记符号** — 即使没有✗，有扣分就说明有错
4. **教师手写正确答案（红笔） > 学生原答案** — 红笔答案就是正确答案，学生写的不一样就是错
5. **总分判断 > 单题判断** — 红笔打的总分如果低于满分，逐题核对哪个题被扣了分

■ 三、常见歧义场景处理

【场景1】题目旁边有红笔写了一个数字，和学生的答案不同
  → 判断：该红笔数字是教师标注的正确答案，学生的答案错误
  → 处理：标记为错题，correctSolution 以红笔数字为准

【场景2】题目旁边有"-3"但没有画✗
  → 判断：教师扣了3分，有错误但可能只扣了部分分
  → 处理：标记为错题，reason 中说明"本题被扣3分，错误可能在..."

【场景3】某道题的红笔标记读不懂或不清晰
  → 判断：对比学生答案和题目要求，如果学生答案明显不合理
  → 处理：标记为错题，reason 中标注"批改标记不清晰，以下分析基于学生答案对比题目要求的判断"

【场景4】整张试卷总分旁边写了一个红笔数字（如总分 120，红笔写 95）
  → 判断：学生得了95分，被扣了25分
  → 处理：结合其他批改标记，定位被扣分的题目

【场景5】一道题有多个小题，只有部分小题有✗
  → 判断：有✗的小题是错的，没✗的小题可能对可能错
  → 处理：只将有明确错误标记的小题列为错题，如果有扣分标记但没有明确指出哪个小题，整个大题归为错题并说明

【场景6】OCR 文本中出现【教师红笔批改：】但内容是某个公式或答案
  → 判断：这是教师写的正确答案
  → 处理：学生答案如果与红笔批改内容不同 → 错题

■ 四、分学科特殊约定

| 学科 | 特殊约定 |
|------|---------|
| 数学/物理/化学 | 红笔批注常见形式：写出正确公式/推导步骤/最终结果。扣分通常按步骤："-2"表示该步骤扣2分 |
| 生物 | 红笔批注常见形式：圈出错误术语、在旁边写出正确术语。填空题常见红笔直接写正确答案 |
| 英语 | 选择题常见：红笔圈出正确答案（如把B改成A并圈A）。写作常见：红笔下划线标出语法错误 + 红笔批注 |
| 语文 | 阅读题常见：红笔批注"要点不全""未结合文本"。文言翻译常见：红笔在错译处下划线并写正确翻译 |

═══════════════════════════════════════`;

