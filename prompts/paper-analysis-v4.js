// ========== 导出 ==========

export function renderPaperAnalysisPrompt(subject, errors) {
  const errorList = errors.map((e, i) => {
    const opt = e.options?.length ? `\n  选项：${e.options.join(' | ')}` : '';
    return `【错题 ${i + 1}】原题号第${e.questionNumber}题（${e.questionType || '未知题型'}）
  题目：${e.questionText || ''}${opt}
  学生作答：${e.studentAnswer || '（未作答）'}
  正确答案：${e.correctAnswer || '（待分析）'}
  批改标记：${e.gradingMark || '无'}
  红笔批注：${e.redInkContent || '无'}
  置信度：${e.confidence || 'high'}`;
  }).join('\n\n');

  return PAPER_ANALYSIS_PROMPT_V4
    .replace('{s}', subject)
    .replace('{errorList}', errorList);
}

