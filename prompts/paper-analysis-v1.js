/** 根据学科渲染完整的分析 Prompt */
export function renderPaperAnalysisPrompt(subject, ocrText) {
  const guide = SUBJECT_GUIDES[subject] || SUBJECT_GUIDES['数学'];
  return PAPER_ANALYSIS_PROMPT_V1
    .replace(/\{s\}/g, subject)
    .replace('{subjectGuide}', guide)
    .replace('{ocrText}', ocrText);
}

